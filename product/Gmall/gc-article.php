              <div class="Article"> 人是生活的奴隶。奴性用凌辱遮住人们的永昼，用血泪淹没了人们的长夜。<br>
            <br>
            从我降生之日到现在已经有七千年了，但我所见到的只是驯服的奴隶，以及用铁链锁着的囚徒。<br>
            <br>
            我走遍了全世界。在生活的道路上，我经历过光明与黑暗，从定居在窑洞里的人到往住在现代建筑里的人我都见过。但至今我所看到的，只有被重负压弯了 的头颅，被铁链锁着的双手和跪在偶像面前的双膝。<br>
            <br>
            我随着人们从巴比伦到巴黎，从尼尼微到纽约。到处我都看见砂地上足印的旁边有镣铐的痕迹，森林和溪谷重复着积年累月的世世代代的呻吟。 <br>
            <br>
            我走进宫殿、学校和庙宇，伫立在宝座、讲台和祭坛的面前。在任何处我都看到：工人是商人的奴隶，商人是军人的奴隶，军人是统治者的奴隶，统治者是 神甫的奴隶，神甫是偶像的奴隶，而偶像则是恶魔所幻化，是骷髅山上的幽灵。 <br>
            <br>
            我走进权贵们的府第，又走进贫贱者的茅舍。我到过装饰着象牙与黄金的华屋，也到过群集住绝望的幽灵与死神的斗室。我看见婴儿从小就养成了奴性，孩 子们一边识字，一边学着服从，小姑娘穿着以温顺、柔和当作衬里的衣服，妇人们躺在屈辱与听命的卧榻上。 <br>
            <br>
            我和一辈又一辈的人们在一起，从刚果河走到幼发拉底河畔，到尼罗河口，到西奈群山，到雅典的广场，罗马的教堂，到君士但丁狭窄的小街，到伦敦一幢 幢高大的楼房。我看见奴性总是和荣誉、尊严并驾齐驱。我看见少年的男女们在祭坛上作为牺牲，奴性被尊视为神；斟上美酒与香露，称颂奴性为统治者；人们在奴 性的圣像前焚香，把他当作先知；在他面前下跪，奉他为金科玉律。在奴性驱使下，人们自相残杀，却把这行为称作爱国，人们在奴性的面前俯首，说奴性是神落下 大地的影子；人们遵从奴性的愿望焚房屋，毁村庄，却说这是平等和友爱；人们竭馨全部的精力和时间，奉献给奴性，说这是财富和经营…… <br>
            <br>
            奴性名目繁多，本质只有一个，它有许多形式，内部却始终如一。奴性——这是自古就有的一种征兆多端的病症；孩子们从父辈那里把它和生命一起承受下 来；岁月把它播种在时代的土壤里，然后收获，就像在一年中的一个季节里收获另一季节的果实。 <br>
            <br>
            这就是我遇到过的奇形怪状的奴性。 <br>
            <br>
            盲目性——它把今天的生活和过去父辈的生活联系在一起；它迫使人们在心灵的传统面前低头；它使年轻的身体充满了陈旧的精神，把它们变作一座座只有 骨灰和腐物的坟墓。 <br>
            <br>
            哑奴性——它把男子配给可憎的女人，把女子变成她所厌恶的男人的床褥；弄得他像脚上穿了一只太窄的鞋，而她就像那肮脏的脚底下的草履。 <br>
            <br>
            聋奴性——它强迫人们迎合人群的口味，涂他们喜欢的颜色，穿他们喜爱的衣服，它把人们的声音变成了回声，把人们的身体变成了影子。 <br>
            <br>
            跛脚的奴性——它迫使强者在骗子手们面前低头；迫使他们的意志服从于野心家们的怪癖；它把他们变成任随播弄的机器。 <br>
            <br>
            白发的奴性——它把孩子们的心灵从阳光普照的高处推往痛苦的深渊；这里贫穷和愚昧并肩徘徊，屈辱和绝望成为比邻；孩子们在苦难中成长，在罪恶里生 活，又染上邪癖而死亡。 <br>
            <br>
            有斑点的奴性——它不按照物品的价值购买东西，不称呼物品本来的名字。它把欺诈叫作聪明，把空谈叫做博学；它说软弱是温顺，说怯懦是威严。 </div>
